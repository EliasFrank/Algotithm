class MedianFinder {
    Queue<Integer> max, min;
    public MedianFinder() {
        min = new PriorityQueue<>();//å°é¡¶å †ä¿å­˜å¤§çš„ä¸€åŠ
        max = new PriorityQueue<>(Comparator.reverseOrder());//å¤§é¡¶å †ä¿å­˜å°çš„ä¸€åŠ
    }
    public void addNum(int num) {
        if (min.size() != max.size()) {
            min.add(num);
            max.add(min.poll());
        } else {
            max.add(num);
            min.add(max.poll());
        }
    }
    public double findMedian() {
        return max.size() == min.size() ? (min.peek() + max.peek()) / 2.0 : min.peek();
    }
}
