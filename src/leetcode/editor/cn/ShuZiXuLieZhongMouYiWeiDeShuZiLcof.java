class Solution {
    public int findNthDigit(int n) {
        int digit = 1;
        long count = 9;
        long start = 1;
        while(n > count) {
            n -= count;
            digit++;
            start *= 10;
            count = 9 * start * digit;
        }
        long num = start + (n -1) / digit;
        int loc = (n - 1) % digit;
        num /= (int)Math.pow(10, digit - loc - 1);
        return (int)num % 10;
    }
}
